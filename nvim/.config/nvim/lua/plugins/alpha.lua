	dashboard.button("h", "у°Ӣ– Help Documents", "<cmd>FzfLua help_tags<CR>"),
	-- dashboard.button("R", "у°‘“ Reload Neovim", "<cmd>restart<CR>"),
	dashboard.button("q", "у°…ҷ Quit Neovim", "<cmd>qa!<CR>"),
}

dashboard.section.footer.val = function()
	local version = vim.version()
	local ver = string.format("v%d.%d.%d", version.major, version.minor, version.patch)
	local scheme = vim.g.colors_name or "default"
	return ver .. "  В·  " .. scheme
end

dashboard.opts.layout = {
	{ type = "padding", val = 1 },
	dashboard.section.header,
	{ type = "padding", val = 1 },
	dashboard.section.buttons,
	{ type = "padding", val = 1 },
	dashboard.section.footer,
}

dashboard.section.buttons.opts.hl = "NonText"
dashboard.section.footer.opts.hl = "Comment"
dashboard.opts.opts.noautocmd = true

alpha.setup(dashboard.opts)
