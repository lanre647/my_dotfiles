local lines = {

	[[  ^  ^  ^   ^вҳҶ вҳ… вҳҶ ___I_вҳҶ вҳ… вҳҶ ^  ^   ^  ^  ^   ^  ^ ]],
	[[ /|\/|\/|\ /|\ вҳ…вҳҶ /\-_--\ вҳҶ вҳ…/|\/|\ /|\/|\/|\ /|\/|\ ]],
	[[ /|\/|\/|\ /|\ вҳ… /  \_-__\вҳҶ вҳ…/|\/|\ /|\/|\/|\ /|\/|\ ]],
	[[ /|\/|\/|\ /|\ у°»Җ |[]| [] | у°»Җ /|\/|\ /|\/|\/|\ /|\/|\ ]],
}
dashboard.section.header.val = lines
dashboard.section.header.opts.hl = {}

for i = 1, #lines do
	local hl_group
	if i <= 2 then
		hl_group = "DiagnosticOk" -- bright green
	elseif i <= 4 then
		hl_group = "String" -- mid green
	elseif i <= 6 then
		hl_group = "Character" -- softer green
	elseif i == 7 then
		hl_group = "NonText"
	else
		hl_group = "Comment" -- dim floor
	end
	table.insert(dashboard.section.header.opts.hl, { { hl_group, 0, #lines[i] } })
end

dashboard.section.buttons.val = {
	dashboard.button("c", "у°ҳі Show All Commands", "<cmd>FzfLua commands<CR>"),
	dashboard.button("f", "уұҸ’ Open Folder", "<cmd>FzfLua files<CR>"),
